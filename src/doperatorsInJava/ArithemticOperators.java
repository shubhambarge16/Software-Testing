package doperatorsInJava;

public class ArithemticOperators 
{
public static void main(String[] args) 
{
//Arithmetic Operators has following types->
//Addition, Subtraction, Multiplication,
//Division, Remainder-->
		
	int a = 25;
	int b = 10;
	int c = a+b;
		
//Different ways of printing statements->
	System.out.println(a+b);	//Addition
	//OR
	System.out.println(c);		//Addition
	//OR
	System.out.println("The Addition is "+c);	//Addition
	//OR
	System.out.println("The Addition is "+(a+b));	//Addition
//-------------------------------------------------------	
	int d = a-b;	//Subtraction
	System.out.println(d);
//---------------------------------------------------------
	int e = a*b;	//Multiplication
	System.out.println(e);
//--------------------------------------------------------	
	int f = a/b;	//Division
	System.out.println(f);
//--------------------------------------------------
	int g = a%b;	//Remainder
	System.out.println(g);
//---------------------------------------------------
	
}
}
